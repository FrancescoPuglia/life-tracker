describe('findMatchingDraftForWeek', () => {
  beforeEach(() => {
    window.localStorage.clear();
  });
  afterEach(() => {
    window.localStorage.clear();
  });

  it('returns the saved draft when no draftIdsInPlay is provided', () => {
    const { draft } = generateWeeklyDraft({
      raw: {
        id: 'r',
        text: 'Lunedì Catalana 2 ore.',
        weekStartISO: WEEK,
        createdAtISO: '2026-01-04T00:00:00.000Z',
      },
      goals: [],
      projects: [],
      tasks: [],
    });
    saveWeeklyPlanDraft({ userIdOrLocal: USER, weekStartISO: WEEK, draft });
    const found = findMatchingDraftForWeek({
      userIdOrLocal: USER,
      weekStartISO: WEEK,
    });
    expect(found?.id).toBe(draft.id);
  });

  it('returns undefined when no draft is saved', () => {
    expect(
      findMatchingDraftForWeek({
        userIdOrLocal: USER,
        weekStartISO: WEEK,
      }),
    ).toBeUndefined();
  });

  it('returns the saved draft when its id is in the draftIdsInPlay set', () => {
    const { draft } = generateWeeklyDraft({
      raw: {
        id: 'r',
        text: 'Lunedì Catalana 2 ore.',
        weekStartISO: WEEK,
        createdAtISO: '2026-01-04T00:00:00.000Z',
      },
      goals: [],
      projects: [],
      tasks: [],
    });
    saveWeeklyPlanDraft({ userIdOrLocal: USER, weekStartISO: WEEK, draft });
    const found = findMatchingDraftForWeek({
      userIdOrLocal: USER,
      weekStartISO: WEEK,
      draftIdsInPlay: [draft.id],
    });
    expect(found?.id).toBe(draft.id);
  });

  it('returns undefined when WPI blocks reference a different draftId', () => {
    const { draft } = generateWeeklyDraft({
      raw: {
        id: 'r',
        text: 'Lunedì Catalana 2 ore.',
        weekStartISO: WEEK,
        createdAtISO: '2026-01-04T00:00:00.000Z',
      },
      goals: [],
      projects: [],
      tasks: [],
    });
    saveWeeklyPlanDraft({ userIdOrLocal: USER, weekStartISO: WEEK, draft });
    expect(
      findMatchingDraftForWeek({
        userIdOrLocal: USER,
        weekStartISO: WEEK,
        draftIdsInPlay: ['some-other-id'],
      }),
    ).toBeUndefined();
  });
});
