const WEEK = '2026-01-05'; // Monday
const USER = 'test-user';

function buildSampleDraftWithCommitted() {
  const { draft } = generateWeeklyDraft({
    raw: {
      id: 'raw-analytics',
      text: 'Lunedì Catalana 2 ore. Martedì Catalana 1 ora.',
      weekStartISO: WEEK,
      createdAtISO: '2026-01-04T20:00:00.000Z',
    },
    goals: [{ id: 'g_chess', title: 'Chess Mastery' }],
    projects: [{ id: 'p_openings', title: 'Openings', goalId: 'g_chess' }],
    tasks: [
      {
        id: 't_catalan',
        title: 'Catalan Course',
        projectId: 'p_openings',
        goalId: 'g_chess',
      },
    ],
  });

  // Materialize each committable block into the TimeBlock-shaped snapshot.
  const wpiBlocks: WpiTimeBlockSnapshot[] = draft.blocks
    .filter((b) => b.mapping?.status === 'mapped')
    .map((b, i) => {
      const payload = draftBlockToTimeBlockInput(b, draft);
      return {
        id: `tb_${i}`,
        title: payload.title,
        startTime: payload.startTime,
        endTime: payload.endTime,
        status: 'planned',
        type: payload.type,
        notes: payload.notes,
        taskId: payload.taskId,
        projectId: payload.projectId,
        goalId: payload.goalId,
      };
    });
  return { draft, wpiBlocks };
}

