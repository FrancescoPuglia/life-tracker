describe('getWeekRange', () => {
  it('returns Monday 00:00 to next Monday 00:00 (half-open)', () => {
    const { start, end } = getWeekRange('2026-01-05');
    expect(start.getFullYear()).toBe(2026);
    expect(start.getMonth()).toBe(0);
    expect(start.getDate()).toBe(5);
    expect(start.getHours()).toBe(0);
    expect(end.getDate()).toBe(12);
  });

  it('throws on malformed weekStartISO', () => {
    expect(() => getWeekRange('nope')).toThrow();
  });
});

describe('isTimeBlockInWeek', () => {
  it('returns true for a Date inside the week and false for outside', () => {
    expect(
      isTimeBlockInWeek(
        { startTime: new Date(2026, 0, 5, 9, 0) },
        '2026-01-05',
      ),
    ).toBe(true);
    expect(
      isTimeBlockInWeek(
        { startTime: new Date(2026, 0, 11, 23, 59) },
        '2026-01-05',
      ),
    ).toBe(true);
    expect(
      isTimeBlockInWeek(
        { startTime: new Date(2026, 0, 12, 0, 0) },
        '2026-01-05',
      ),
    ).toBe(false);
    expect(
      isTimeBlockInWeek(
        { startTime: new Date(2026, 0, 4, 23, 59) },
        '2026-01-05',
      ),
    ).toBe(false);
  });

  it('accepts ISO strings', () => {
    expect(
      isTimeBlockInWeek(
        { startTime: '2026-01-06T10:00:00.000Z' },
        '2026-01-05',
      ),
    ).toBe(true);
  });
});

describe('getTimeBlockDurationMinutes', () => {
  it('returns minute difference between start and end', () => {
    const s = new Date(2026, 0, 5, 9, 0);
    const e = new Date(2026, 0, 5, 10, 30);
    expect(
      getTimeBlockDurationMinutes({ startTime: s, endTime: e }),
    ).toBe(90);
  });

  it('returns 0 on inverted or malformed dates', () => {
    expect(
      getTimeBlockDurationMinutes({
        startTime: new Date(2026, 0, 5, 10),
        endTime: new Date(2026, 0, 5, 9),
      }),
    ).toBe(0);
    expect(
      getTimeBlockDurationMinutes({
        startTime: 'bad-date',
        endTime: 'bad-date',
      }),
    ).toBe(0);
  });
});

