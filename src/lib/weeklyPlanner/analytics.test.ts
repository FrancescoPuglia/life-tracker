describe('calculateWpiWeeklyAnalytics', () => {
  it('counts only WPI-tagged blocks inside the week', () => {
    const { draft, wpiBlocks } = buildSampleDraftWithCommitted();
    // Add a non-WPI block to confirm it's ignored.
    const noise: WpiTimeBlockSnapshot = {
      id: 'noise',
      title: 'Manual TimeBlock',
      startTime: new Date(2026, 0, 5, 14, 0),
      endTime: new Date(2026, 0, 5, 15, 0),
      status: 'completed',
      notes: 'no key here',
    };
    const result = calculateWpiWeeklyAnalytics({
      allTimeBlocks: [...wpiBlocks, noise],
      weekStartISO: WEEK,
      matchedDraft: draft,
    });
    expect(result.weekly.generatedBlocks).toBe(wpiBlocks.length);
    expect(result.weekly.generatedBlocks).toBeGreaterThan(0);
    expect(result.wpiBlocks).toHaveLength(wpiBlocks.length);
  });

  it('classifies statuses: completed/overrun → completed, cancelled → missed, planned/in_progress → pending', () => {
    const { draft, wpiBlocks } = buildSampleDraftWithCommitted();
    if (wpiBlocks.length < 4) return;
    const withStatuses = wpiBlocks.map((b, i) => {
      const status =
        i === 0
          ? 'completed'
          : i === 1
            ? 'overrun'
            : i === 2
              ? 'cancelled'
              : 'planned';
      return { ...b, status };
    });
    const result = calculateWpiWeeklyAnalytics({
      allTimeBlocks: withStatuses,
      weekStartISO: WEEK,
      matchedDraft: draft,
    });
    expect(result.weekly.completedBlocks).toBe(2);
    expect(result.weekly.missedBlocks).toBe(1);
    expect(result.weekly.plannedBlocks).toBeGreaterThanOrEqual(1);
  });

  it('reports goal coverage and average block minutes', () => {
    const { draft, wpiBlocks } = buildSampleDraftWithCommitted();
    const result = calculateWpiWeeklyAnalytics({
      allTimeBlocks: wpiBlocks,
      weekStartISO: WEEK,
      matchedDraft: draft,
    });
    expect(result.weekly.goalCoverageRate).toBe(1);
    expect(result.weekly.averageBlockMinutes).toBeGreaterThan(0);
    expect(result.weekly.totalPlannedMinutes).toBeGreaterThan(0);
  });

  it('partialBlocks is always 0 because TimeBlockStatus has no partial', () => {
    const { draft, wpiBlocks } = buildSampleDraftWithCommitted();
    const result = calculateWpiWeeklyAnalytics({
      allTimeBlocks: wpiBlocks,
      weekStartISO: WEEK,
      matchedDraft: draft,
    });
    expect(result.weekly.partialBlocks).toBe(0);
    expect(
      result.weekly.notes.some((n) => /partial/i.test(n)),
    ).toBe(true);
  });

  it('day breakdown sums to totalPlannedMinutes', () => {
    const { draft, wpiBlocks } = buildSampleDraftWithCommitted();
    const result = calculateWpiWeeklyAnalytics({
      allTimeBlocks: wpiBlocks,
      weekStartISO: WEEK,
      matchedDraft: draft,
    });
    const sumDays = (
      [0, 1, 2, 3, 4, 5, 6] as const
    ).reduce(
      (acc, d) => acc + result.weekly.dayBreakdown[d].plannedMinutes,
      0,
    );
    expect(sumDays).toBe(result.weekly.totalPlannedMinutes);
  });
});

